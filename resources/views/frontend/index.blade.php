<script>
$(function(){
    $('#submit').click(function(){
        $('.response-msg').text('').hide();
        $(this).attr('disabled',true);
        var name = $('#name').val();
        var email = $('#email').val();
        var phone = $('#phone').val();
        if(name == ''){
            $('.response-msg').text('Name is required.').show();
        }else if(phone == ''){
            $('.response-msg').text('Phone is required.').show();
        }else if(email == ''){
            $('.response-msg').text('Email is required.').show();
        }else{
            $('.response-msg').text('').hide();
            $('#loading').css('display','block');
            $.ajax({
                url: '/request-call-back',
                type: 'post',
                data: {_token:'{{csrf_token()}}',name:name,email:email,phone:phone},
                success: function(response){
                    var name = $('#name').val('');
                    var email = $('#email').val('');
                    var phone = $('#phone').val('');
                    $('#loading').css('display','none');
                    $('.response-msg').text(response.message).show();
                    $(this).attr('disabled',false);
                }
            });
        }
    })
});
